# توكن بوت التليجرام (احصل عليه من @BotFather)
TELEGRAM_TOKEN=ضع_توكن_البوت_هنا

# اختر AI Provider (gemini أو groq)
AI_PROVIDER=gemini

# مفتاح Gemini API (احصل عليه من https://aistudio.google.com/apikey)
GEMINI_API_KEY=ضع_مفتاح_Gemini_هنا

# مفتاح Groq API (احصل عليه من https://console.groq.com)
GROQ_API_KEY=ضع_مفتاح_Groq_هنا


# ════════════════════════════════════════════════════════════════
# مثال على الشكل الصحيح:
# ════════════════════════════════════════════════════════════════
# 
# TELEGRAM_TOKEN=7234567890:AAHfB3xxxxxxxxxxxxxxxxxxxxxxxxxxxxxx
# AI_PROVIDER=gemini
# GEMINI_API_KEY=AIzaSyxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx
# GROQ_API_KEY=gsk_xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx
# 
# ════════════════════════════════════════════════════════════════
# ملاحظات:
# ════════════════════════════════════════════════════════════════
# 
# 1. AI_PROVIDER: اختر gemini أو groq
#    - gemini: يستخدم Google Gemini (مجاني ومميز)
#    - groq: يستخدم Groq Llama (سريع جداً ومجاني)
# 
# 2. يمكنك وضع المفتاحين معاً والتبديل بينهم بتغيير AI_PROVIDER
# 
# 3. للحصول على Gemini API Key:
#    - اذهب إلى: https://aistudio.google.com/apikey
#    - سجل دخول بحساب Google
#    - اضغط "Create API Key"
#    - مجاني 100%!
# 
# 4. للحصول على Groq API Key:
#    - اذهب إلى: https://console.groq.com
#    - سجل بحساب Google أو GitHub
#    - اذهب لـ API Keys
#    - اضغط "Create API Key"
#    - مجاني أيضاً!
# 
# 5. Gemini vs Groq:
#    - Gemini: أفضل في الفهم والردود الطويلة
#    - Groq: أسرع في الاستجابة (ممتاز للبوتات)
# 
